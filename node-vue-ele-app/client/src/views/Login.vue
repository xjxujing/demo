<template>
  <div class="login">
    <section class="form_container">
      <div class="manage_tip">
        <span class="title">后台资金流水管理系统</span>
        <!-- status-icon	是否在输入框中显示校验结果反馈图标 -->
        <!-- rules	表单验证规则 -->
        <!-- model	表单数据对象 -->
        <!-- ref 帮助获取表单内容 -->
        <el-form
          :model="loginUser"
          :rules="rules"
          ref="loginForm"
          label-width="100px"
          class="loginForm"
        >
          <el-form-item label="邮箱" prop="email">
            <el-input type="email" v-model="loginUser.email" placeholder="请输入邮箱"></el-input>
          </el-form-item>
          <el-form-item label="密码" prop="password">
            <el-input type="password" v-model="loginUser.password" placeholder="请输入密码"></el-input>
          </el-form-item>

          <el-form-item>
            <el-button type="primary" class="submit_btn" @click="submitFrom('loginForm')">登录</el-button>
          </el-form-item>

          <div class="tiparea">
            <p>
              还没有账号?现在
              <router-link to="/register">注册</router-link>
            </p>
          </div>
        </el-form>
      </div>
    </section>
  </div>
</template>

<script>
import jwt_decode from "jwt-decode";

export default {
  name: "login",
  // data开始
  data() {
    return {
      loginUser: {
        email: "",
        password: ""
      },
      rules: {
        // required: 是否必须填写, message:"提示信息", trigger: 何时出发
        email: [
          {
            type: "email",
            required: true,
            message: "邮箱格式不正确",
            trigger: "blur"
          }
        ],
        password: [
          { required: true, message: "密码不能为空", trigger: "blur" },
          { min: 6, max: 30, message: "长度在6到30之间", trigger: "blur" }
        ]
      }
    };
  },
  //   data结束
  methods: {
    submitFrom: function(formName) {
      this.$refs[formName].validate(valid => {
        if (valid) {
          this.$axios.post("api/users/login", this.loginUser).then(res => {
            // 存储token
            const { token } = res.data;
            localStorage.setItem("eleToken", token);
            // 解析token
            const decode = jwt_decode(token);

            // token存储到vuex中
            this.$store.dispatch("setAuthenticated", !this.isEmpty(decode));
            this.$store.dispatch("setUser", decode);
            this.$router.push("/index");
          });
        } else {
          console.log("error submit!!");
          return false;
        }
      });
    },
    isEmpty: function(value) {
      return (
        value === undefined ||
        value === null ||
        (typeof value === "object" && Object.keys(value).length === 0) ||
        (typeof value === "string" && value.trim().length === 0)
      );
    }
  },
  components: {}
};
</script>

